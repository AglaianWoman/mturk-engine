import * as React from 'react';
import { Card, Stack, DisplayText, ButtonGroup, Button } from '@shopify/polaris';

export interface Props {
  reward: string;
  groupId: string;
}

const Actionable = ({ reward, groupId }: Props) => {
  const pandaUrl = `https://www.mturk.com/mturk/previewandaccept?groupId=${groupId}`;
  const previewUrl = `https://www.mturk.com/mturk/preview?groupId=${groupId}`;

  return (
    <Card.Section>
      <Stack>
        <ButtonGroup segmented>
          <Button external url={previewUrl} icon="view" />
          <Button primary external url={pandaUrl} icon="add" />
        </ButtonGroup>
        <DisplayText>{reward}</DisplayText>
      </Stack>
    </Card.Section>
  );
};

export default Actionable;
